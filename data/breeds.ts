export const getBreedBySlug = (slug: string): Breed | undefined =>
  breeds.find((b) => b.slug === slug);

export const getBreedsByPetType = (petType: "dog" | "cat"): Breed[] =>
  breeds.filter((b) => b.petType === petType);

export const getCheapestBreeds = (petType: "dog" | "cat", limit = 5): Breed[] =>
  breeds
    .filter((b) => b.petType === petType)
    .sort((a, b) => a.costIndex - b.costIndex)
    .slice(0, limit);

